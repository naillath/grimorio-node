// ❌ Más práticas: nome genérico e sem contexto

//  Nomes em outro idioma, além de inglês, podem dificultar a compreensão do código para outros desenvolvedores.

let nome = 'João'; // Lembre-se de usar nomses em inglês para garantir que seu código seja compreendido por uma audiência mais ampla.

// Abreviações podem ser confusas e não transmitir claramente o propósito da variável.

let x = 10; // O que é 'x'? Não sabemos o que representa.
let y = 20; // O que é 'y'? Não sabemos o que representa.

// Enumeração, evite enumerar suas variáveis, isso pode causar confusão e dificultar a leitura do código.

let a1 = 'João';
let a2 = 'Maria';
let a3 = 'Pedro';   

// Incoerência de tipo, você declara como nome, mas depois atribui um valor de tipo diferente, isso pode causar erros e confusão.

let text = 30; // O nome 'text' sugere que deveria ser uma string, mas está sendo atribuído um número. Isso pode levar a erros e confusão no código.
