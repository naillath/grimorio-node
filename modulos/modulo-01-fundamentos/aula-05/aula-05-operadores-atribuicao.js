// Operadores de Atribuição

// Atribuição Simples (=)
let x = 10;
console.log("Valor de x:", x); // Saída: Valor de x: 10 
// Atribuição de Adição (+=)
x += 5;
console.log("Valor de x após adição:", x); // Saída: Valor de x após adição: 15 

// Atribuição de Subtração (-=)
x -= 3;
console.log("Valor de x após subtração:", x); // Saída: Valor de x após subtração: 12 

// Atribuição de Multiplicação (*=)
x *= 2;
console.log("Valor de x após multiplicação:", x); // Saída: Valor de x após multiplicação: 24 

// Atribuição de Divisão (/=)
x /= 4;
console.log("Valor de x após divisão:", x); // Saída: Valor de x após divisão: 6 

// Atribuição de Módulo (%=)
x %= 5;
console.log("Valor de x após módulo:", x); // Saída: Valor de x após módulo: 1