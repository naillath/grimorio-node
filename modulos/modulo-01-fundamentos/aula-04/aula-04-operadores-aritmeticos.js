// Operadores Aritméticos

// Adição (+)
// Subtração (-)
// Multiplicação (*)
// Divisão (/)
// Módulo (%)


// Adição
let a = 10;
let b = 5;
let soma = a + b;
console.log("Soma:", soma); // Saída: Soma: 15

// Subtração
let subtracao = a - b;
console.log("Subtração:", subtracao); // Saída: Subtração: 5    

// Multiplicação
let multiplicacao = a * b;
console.log("Multiplicação:", multiplicacao); // Saída: Multiplicação: 50

// Divisão
let divisao = a / b;
console.log("Divisão:", divisao); // Saída: Divisão: 2

// Módulo (resto da divisão) - Utilizado para verificar se um número é par ou ímpar.
let modulo = a % b;
console.log("Módulo:", modulo); // Saída: Módulo: 0