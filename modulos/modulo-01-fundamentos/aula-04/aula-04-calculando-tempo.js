// Calculando o tempo em segundos

console.log(`Um minuto tem ${60} segundos.`);
console.log(`Uma hora tem ${60 * 60} segundos.`);
console.log(`Um dia tem ${60 * 60 * 24} segundos.`);
console.log(`Uma semana tem ${60 * 60 * 24 * 7} segundos.`);
console.log(`Um ano tem ${60 * 60 * 24 * 365} segundos.`);

const minutesInSecond = 60;
const hoursInSecond = 60 * minutesInSecond;
const dayInSecond = 24 * hoursInSecond;
const weekInSecond = 7 * dayInSecond;
const yearInSecond = 365 * dayInSecond; 

console.log(`Um minuto tem ${minutesInSecond} segundos.`);
console.log(`Uma hora tem ${hoursInSecond} segundos.`);
console.log(`Um dia tem ${dayInSecond} segundos.`);
console.log(`Uma semana tem ${weekInSecond} segundos.`);
console.log(`Um ano tem ${yearInSecond} segundos.`);