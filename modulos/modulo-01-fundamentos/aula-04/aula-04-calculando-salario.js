// Calculando salário

const salary = 1412;

console.log("Por mês: ", salary); // Saída: Por mês: 1412
console.log("Em um ano: ", salary * 12); // Saída: Em um ano: 16944
console.log("Por semana (1 mês): ", salary / 4); // Saída: Por semana (1 mês): 353
console.log("Por dia (1 mês): ", salary / 30); // Saída: Por dia (1 mês): 47.06666666666667 
