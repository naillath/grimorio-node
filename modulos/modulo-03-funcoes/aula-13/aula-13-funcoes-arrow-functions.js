// Arrow functions - uma sintaxe mais concisa para escrever funções em JavaScript. Elas são especialmente úteis para funções anônimas e funções de callback.

const sum = (a, b) => {
    return a + b;
}

const resultSum = sum(7, 3);
console.log(resultSum);


// Encurtamente de arrow function

const sub = (a, b) => a - b;

const resultSub = sub(7, 3);
console.log(resultSub);