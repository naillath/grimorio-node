// Estrutura de repetição while - Usado em situações de loops/repetições em que você não sabe quantas vezes algo precisa se repetir, mas sabe a condição de parada.let count = 0;

while (count < 10) { // conta de 0 a 9
    console.log(count);
    count++;
}

let countReverse = 10;

while (countReverse > 0) { // conta de 10 a 1
    console.log(countReverse);
    countReverse--;
}